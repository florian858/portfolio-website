(function () {
  var select = document.getElementById('lang');
  function setLang(l) {
    var nodes = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < nodes.length; i++) {
      var k = nodes[i].getAttribute('data-i18n');
      if (dict[l] && dict[l][k]) nodes[i].textContent = dict[l][k];
    }

    // ook <title> updaten
    var titleEl = document.querySelector("title[data-i18n]");
    if (titleEl) {
      var tk = titleEl.getAttribute("data-i18n");
      if (dict[l] && dict[l][tk]) document.title = dict[l][tk];
    }

    localStorage.setItem('lang', l);
    document.documentElement.setAttribute('lang', l);
  }

  select.addEventListener('change', function (e) {
    setLang(e.target.value);
  });

  var navLang = (navigator.language || 'nl').slice(0, 2).toLowerCase();
  var initial = localStorage.getItem('lang') || (dict[navLang] ? navLang : 'nl');
  setLang(initial);
  select.value = initial;
})();
